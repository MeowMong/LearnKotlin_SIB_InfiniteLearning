fun main() {
    // Looping dengan While Do
    println("\nFor dengan While Do \t\t: ")
    var i = 0
    while (i < 10) {
        println("Loop to: $1")
        i++
    }

    // Looping dengan Do While
    println("\nFor dengan Do While \t\t: ")
    var k = 0
    do {
        println("$k Kampus Merdeka")
        k++
    } while (i <= 8)

}
